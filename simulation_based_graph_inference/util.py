from matplotlib import pyplot as plt
import networkx as nx
import typing
from .convert import to_networkx
from .generators import set_seed


def _plot_generated_graph(generator: typing.Callable, num_nodes: int, *args) -> None:
    """
    Plot a graph generated by the generator.
    """
    seed = 0
    set_seed(seed)
    graph = generator(num_nodes, *args)
    graph = to_networkx(graph)
    fig, ax = plt.subplots()
    pos = nx.spring_layout(graph, seed=seed)
    nx.draw_networkx_edges(graph, pos, edge_color='gray')
    nx.draw_networkx_nodes(graph, pos, node_color='#7fbde9', edgecolors='C0')
    nx.draw_networkx_labels(graph, pos)
    ax.set_aspect('equal')
    ax.set_axis_off()
    fig.tight_layout()
